import { createCookieSessionStorage, redirect } from '@remix-run/node';
import { prisma } from './database.server';
import { compare, hash } from 'bcryptjs';

const SESSION_SECRET = process.env.SESSION_SECRET;

// Cookie settings
const sessionStorage = createCookieSessionStorage({
  cookie: {
    secure: process.env.NODE_ENV === 'production', // to bypass https requirement when in production mode. This is a nodejs settings. Not specific to Remix
    secrets: [SESSION_SECRET], // To crosscheck with the session received from front-end is the same as back-end
    sameSite: 'lax', // Protecting users from making unwilling/unknown requests generated by third party sites
    maxAge: 30 * 24 * 60 * 60, // Cookie validation. This is timespan calculated against seconds. This equation grants the cookie validation for 30 days
    httpOnly: true, // To ensure client side javascript cannot access this cookie
  },
});

async function createUserSession(userId, redirectPath) {
  const session = await sessionStorage.getSession();
  // getSession() is a function provided by Remix to handle session for us
  // this function yields a promise hence the await keyword
  session.set('userId', userId);
  return redirect(redirectPath, {
    headers: {
      'Set-Cookie': await sessionStorage.commitSession(session),
    },
  });
}

// While other functions here are backend related, this one in particular is for the front-end
// This function provides information on userId along with its valid cookie so that the front-end can extract it via loader() and grant appropriate access according to the user's level of clearance
// This function is being extracted under loader() in __marketing.jsx
// See #96
export async function getUserFromSession(request) {
  // Remix will 'decode' this cookie using the secret that we've created in sessionStorage. see #96
  const session = await sessionStorage.getSession(
    request.headers.get('Cookie')
  );

  const userId = session.get('userId'); // We know there's userId in the cookie because we created it in createUserSession

  if (!userId) {
    return null;
  }

  return userId;
}

// Just as creating a cookie, we need to destroy it once the user logout
// We use this function inside of logout.js to attach it with logout
export async function destroyUserSession(request) {
  const session = await sessionStorage.getSession(
    request.headers.get('Cookie')
  );

  return redirect('/', {
    headers: {
      'Set-Cookie': await sessionStorage.destroySession(session),
    },
  });
}

// This function is used to prevent access to anyone without proper clearance
// This is being called in expenses.jsx
export async function requireUserSession(request) {
  const userId = await getUserFromSession(request);

  if (!userId) {
    // Anytime Remix needs to execute a throw redirect, it halts all other functions/operations where requireUserSession() is being called
    // See #98
    throw redirect('/auth?mode=login');
  }

  return userId;
}

export async function signup({ email, password }) {
  const existingUser = await prisma.user.findFirst({ where: { email } });

  if (existingUser) {
    const error = new Error('User with provided email already exists');
    error.status = 422;
    throw error;
  }

  // hash is an asynchronous function that returns a promise. Hence the await keyword infront of it
  const hashedPassword = await hash(password, 12);
  // The second argument is the number of salt rounds
  // In our case we set it to 12

  const user = await prisma.user.create({
    data: { email: email, password: hashedPassword },
  });

  return createUserSession(user.id, '/expenses');
}

export async function login({ email, password }) {
  // A user only gets an assigned cookie if they pass the checklists below(refer to the if blocks).
  const existingUser = await prisma.user.findFirst({ where: { email } });
  const matchingPass = await compare(password, existingUser.password);
  // compare function that comes from bcrypt.js package is used to compare entered password against the hashed password of the user in the database.
  // See #93

  if (!existingUser) {
    // This is a manual error being set.
    // Right now this will trigger the error boundary component, not the catch boundary component
    const error = new Error(
      'Could not log you in. Please check your credentials'
    );
    error.status = 401;
    throw error;
  }

  if (!matchingPass) {
    const error = new Error(
      'Unmatched password. Please check your credentials'
    );
    error.status = 401;
    throw error;
  }

  return createUserSession(existingUser.id, '/expenses');
}
